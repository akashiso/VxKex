IID2D1PixelEffectImpl* PixelEffectImpl_Init(const GUID* guid, const BYTE* shader, UINT32 szShader, UINT32 szCb, UINT numInput)
{
	IID2D1PixelEffectImpl* i;

	i = (IID2D1PixelEffectImpl*)CoTaskMemAlloc(sizeof(IID2D1PixelEffectImpl));
	if (i == NULL)
		return NULL;

	i->lpVtbl = PixelEffectImplVtbl;
	i->shaderCompiled = shader;
	i->szShaderCompiled = szShader;
	i->shaderGuid = guid;
	i->RefCount = 1;
	i->szConstantBuffer = szCb;

	i->node.numInput = numInput;

	if (szCb != 0)
	{
		i->constantBuffer = CoTaskMemAlloc(szCb);
		if (i->constantBuffer == NULL)
		{
			CoTaskMemFree(i);
			return NULL;
		}
	}
	else
		i->constantBuffer = NULL;

	return i;
}

